    private String getMonthYearString(int month, int year) {
    String[] monthNames = {"January", "February", "March", "April", "May", "June",
                           "July", "August", "September", "October", "November", "December"};
    return monthNames[month - 1] + " " + year;  // âœ… Ensures output like "June 2025"
}


    private void loadEmployeePayslip(String empNo) {
    System.out.println("Loading payslip for Employee ID: " + empNo);

    // Retrieve Employee Details from File/Database
    Employee e = EmployeeFileHandler.getEmployee(Integer.parseInt(empNo)).orElse(null);

    if (e != null) {
        employeeNameLabel.setText(e.getFirstName() + " " + e.getLastName());
        employeeNumber.setText(empNo);
        position.setText(e.getPosition());
        employmentStatus.setText(e.getStatus());
        sssNumber.setText(e.getSssNumber());
        tinNumber.setText(e.getTinNumber());
        philhealthNumber.setText(e.getPhilHealthNumber());
        pagIbigNumber.setText(e.getPagIbigNumber());

        // âœ… Set Allowance Fields
        double phone = e.getPhoneAllowance();
        double clothing = e.getClothingAllowance();
        double rice = e.getRiceSubsidy();

        phoneAllowance.setText(String.format("â‚±%,.2f", phone));
        clothingAllowance.setText(String.format("â‚±%,.2f", clothing));
        riceSubsidy.setText(String.format("â‚±%,.2f", rice));

        // âœ… Set Hourly Rate (Restored)
        String cleanedRate = String.format("%.2f", e.getHourlyRate()).replaceAll("[^0-9.]", "");
        hourlyRate.setText(cleanedRate);

        // âœ… Compute Total Benefits
        double totalBenefits = phone + clothing + rice;
        benefitsLabel.setText(String.format("â‚±%,.2f", totalBenefits));
    } else {
        JOptionPane.showMessageDialog(this, "Employee not found!", "Lookup Error", JOptionPane.ERROR_MESSAGE);
    }
}









